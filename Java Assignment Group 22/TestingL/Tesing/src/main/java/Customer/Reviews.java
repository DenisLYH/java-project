    public static void loadReviews(JTable jTable1) {
       String reviewFilePath = "reviews.txt";
        Gson gson = new Gson();
        List<Review> reviews = new ArrayList<>();

        try (BufferedReader br = new BufferedReader(new FileReader(reviewFilePath))) {
            String line;
            while ((line = br.readLine()) != null) {
                Review review = gson.fromJson(line, Review.class); // Parse each line separately
                reviews.add(review);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

        // âœ… Populate JTable
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear previous data

        for (Review review : reviews) {
            model.addRow(new Object[]{review.customerID, review.entityType, review.rating, review.comment});
        }
    }
